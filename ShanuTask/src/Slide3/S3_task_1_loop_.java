package Slide3;

public class S3_task_1_loop_ {

	public static void main(String[] args) {
		
		 
		 
		
		
		//  1. Print 2 to -10 
			for(int i=2;i>=-10;i--) {
				System.out.println("the value of i is "+i);
				
			}
		// 2. Print 3 to 20; 
			for(int a=3; a<=20;a++) {
				System.out.println("the value of a = "+a);
			}
		//	3. Print -355 to -255
			for(int b=-355;b<=-255;b++) {
				System.out.println("the value of b = "+b);
			}
		// 4. Print -956 to -960
			for(int c=-956;c>=-960;c--) {
				System.out.println("the value of e is "+ c);
			}
		//5. print 10 to 10
			for(int d=-10;d<=10;d++) {
				
				System.out.println("the value of d = "+d);
				if(d==5) {
					break;
				}
			}
		//6. Print -5 to 5 and break if the value is 3
			for (int e=-5;e<=5;e++) {
				
				System.out.println("the value of e = "+e);
				if(e==3) {
					break;
				}
			}
		// 7. Print -500 to -200 and break if the value of the variable is -278
			for(int x=-500;x<=-200;x++) {
				
				System.out.println("the value of x is "+ x);
				if (x==-278) {
					break;
				}
				}
		
		 
		 
	}

}
